    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed

        String nombreCuenta = nombreCuentaField.getText();
        String montoIngreso = montoIngresoField.getText();
        

        //String successMsg = isEdition ? "modificado" : "registrado";
        //String errorMsg = isEdition ? "modificar" : "registrar";

        // Validaciones para los campos
        if (nombreCuenta.isEmpty() || montoIngreso.isEmpty()) {
            
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            montoIngresoField.requestFocus();
            
        } else { // codigo donde se agrega
            
            try {
                //VALIDAR: El monto de igreso no debe superar los 20000
                double montoIngresoDouble = Double.parseDouble(montoIngreso);
                if(montoIngresoDouble > 20000){
                    javax.swing.JOptionPane.showMessageDialog(this, "El monto ingresado no puede ser mayor a 20 000.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
                    montoIngresoField.requestFocus();
                    return;
                }
                
                boolean confirmarDatosRegistroIngresoBancario = controladorRegistroIngresoBancario.confirmarDatosRegistroIngresoBancario(bankAccount, montoIngresoDouble);
                if (confirmarDatosRegistroIngresoBancario) {
                    controladorRegistroIngresoBancario.registrarIngresoBancario(bankAccount, montoIngresoDouble);
                } else {
                    return;
                }
                javax.swing.JOptionPane.showMessageDialog(this, "Ingreso bancario registrado exitosamente.\n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Ocurri√≥ un error al registrar el ingreso bancario. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_registerButtonActionPerformed


